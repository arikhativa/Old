int		ft_sqrt(int nb)
{
	int n;

	n = 2;
	if (nb == 4)
		return (2);
	while (n < 46341 && n < nb / 2)
	{
		if (n * n == nb)
			return (n);
		n++;
	}
	return (0);
}
